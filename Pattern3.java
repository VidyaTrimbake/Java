import java.util.*;

public class Pattern3 {
    public static void main(String a[]){
        int iCol = 4;
        int iRow = 4;

        for(int i=1; i<=iRow; i++){
            for(int j=1; j<=iCol; j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
